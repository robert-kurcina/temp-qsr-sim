const fs = require('fs');
const path = require('path');

const dataDir = path.join(__dirname, '..', 'src', 'data');
const outputDir = path.join(__dirname, '..', 'src', 'lib');
const outputFile = path.join(outputDir, 'data.ts');

const itemDataFiles = [
    'armors.json',
    'bow_weapons.json',
    'equipment.json',
    'grenade_weapons.json',
    'melee_weapons.json',
    'ranged_weapons.json',
    'support_weapons.json',
    'thrown_weapons.json'
];
const itemDataFileSet = new Set(itemDataFiles);

// NO ALIASES - Use exact item names only
// All item references in code and configuration must use the exact name from JSON files

function normalizeItemData(data, fileName) {
  if (!data || typeof data !== 'object' || Array.isArray(data)) {
    return data;
  }

  for (const itemName in data) {
    if (!Object.prototype.hasOwnProperty.call(data, itemName)) {
      continue;
    }

    const item = data[itemName];
    if (!item || typeof item !== 'object' || Array.isArray(item)) {
      continue;
    }

    if (item.class) {
      // Split class into classification and type
      const classParts = item.class.split(' - ');
      item.classification = classParts[0];
      item.type = classParts.length > 1 ? classParts[1] : classParts[0];
    } else {
      console.log(`Item "${itemName}" in file "${fileName}" is missing the 'class' property.`);
    }

    // Trim whitespace from traits
    if (item.traits && Array.isArray(item.traits)) {
      item.traits = item.traits.map(trait => (typeof trait === 'string' ? trait.trim() : trait));
    }
  }

  return data;
}

function loadBundledDataFromJson(inputDir = dataDir) {
  const bundledData = {};
  const jsonFiles = fs.readdirSync(inputDir)
    .filter(file => file.endsWith('.json'))
    .sort((a, b) => a.localeCompare(b));

  for (const file of jsonFiles) {
    const filePath = path.join(inputDir, file);
    const fileName = path.basename(file, '.json');
    const fileContent = fs.readFileSync(filePath, 'utf-8');
    const parsedData = JSON.parse(fileContent);
    const normalizedData = itemDataFileSet.has(file)
      ? normalizeItemData(parsedData, fileName)
      : parsedData;

    bundledData[fileName] = normalizedData;
  }

  return bundledData;
}

function createOutputContent(bundledData) {
  return `// This file is generated by scripts/bundle-data.cjs
// Do not edit this file directly.
// 
// ITEM NAMING CONVENTION:
// - Use EXACT item names from JSON files (no aliases, no abbreviations)
// - Examples: "Daggers", "Spear, Medium", "Sword, Broad", "Shield, Medium"
// - See src/data/*.json for complete item lists

export const gameData = ${JSON.stringify(bundledData, null, 2)};
`;
}

function bundleData(options = {}) {
  const resolvedDataDir = options.dataDir || dataDir;
  const resolvedOutputDir = options.outputDir || outputDir;
  const resolvedOutputFile = options.outputFile || outputFile;
  const bundledData = loadBundledDataFromJson(resolvedDataDir);
  const outputContent = createOutputContent(bundledData);

  if (!fs.existsSync(resolvedOutputDir)) {
    fs.mkdirSync(resolvedOutputDir, { recursive: true });
  }

  fs.writeFileSync(resolvedOutputFile, outputContent);
  return { bundledData, outputFile: resolvedOutputFile, outputContent };
}

if (require.main === module) {
  bundleData();
  console.log('Game data bundled successfully!');
  console.log('NOTE: Use exact item names only - no aliases or abbreviations.');
}

module.exports = {
  itemDataFiles,
  loadBundledDataFromJson,
  createOutputContent,
  bundleData,
};
